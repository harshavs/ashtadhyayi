स्वरमात्राः = {'अ' : ' ', 
          'आ': 'ा', 
          'इ' : 'ि',
          'ई': 'ी', 
          'उ' : 'ु', 
          'ऊ':'ू', 
          'ऋ' : 'ृ', 
          'ॠ' : 'ॄ', 
          'ऌ' : 'ॢ', 
          'ए' : 'े', 
          'ऐ' : 'ै', 
          'ओ' : 'ो', 
          'औ' : 'ौ', 
          'अं' : 'ं', 
          'अः' : 'ः'}

def स्वरात्_मात्रा(स्वराः):
    return ''.join(स्वरमात्राः.get(स्वरः)  for स्वरः in स्वराः)
